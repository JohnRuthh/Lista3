def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

if __name__ == "__main__":
    print("Adding 1 and 2 gives:", add(1, 2))
    print("Subtracting 2 from 3 gives:", subtract(3, 2))
    print("Multiplying 2 and 2 gives:", multiply(2, 2))
    print("Dividing 15 by 3 gives:", divide(15, 3))
